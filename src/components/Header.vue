<template>
    <header class="header">
        <h1>todos</h1>
        <div>test22</div>
        <input 
        class="new-todo"
        v-model="text"
        @keypress="handleTodo"
         autofocus autocomplete="off" placeholder="write something"/>
    </header>
</template>

<script>
export default {
    data() {
        return {
            text: ""
        }
    },
    methods: {
        handleTodo({keyCode}) {
            const {text} = this;

            if(keyCode ===13 && text !=="") {
                this.$emit("insertTodo", text);
                this.text= "";
            }
        }
    }
};
</script>
